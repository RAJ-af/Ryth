"""RDE configuration — poore pipeline ka single source of truth.

Isi ko badal ke aap 30M ya 10B model ke liye data banao — RDS format same rehta
hai, sirf `tokenizer_version` / `seq_len` / `vocab_size` badalte hain.
"""

from __future__ import annotations

from dataclasses import dataclass, field


# path me ye folders aaye to file drop kar do (vendor / build artifacts)
DEFAULT_VENDOR_DIRS = {
    "node_modules", "vendor", "third_party", "thirdparty", "dist", "build",
    ".git", ".svn", "venv", ".venv", "env", "__pycache__", "site-packages",
    ".mypy_cache", ".pytest_cache", "target", "bower_components", ".tox",
}

# ye markers dikhein to file "generated" maano
DEFAULT_GENERATED_MARKERS = (
    "do not edit", "@generated", "autogenerated", "auto-generated",
    "generated by", "this file was automatically generated", "code generated by",
)

# in extensions ko text-code samjho (baaki -> validator decide karega)
DEFAULT_ALLOWED_EXT = {
    ".py", ".md", ".json", ".yaml", ".yml", ".toml", ".sh", ".bash",
    ".txt", ".cfg", ".ini", ".dockerfile",
}


@dataclass
class RDEConfig:
    # --- chunking ---
    seq_len: int = 4096
    overlap: int = 0                 # sliding-window overlap (tokens)

    # --- tokenizer / vocab ---
    vocab_size: int = 32768
    tokenizer_version: int = 1
    add_bos: bool = True
    add_eos: bool = True

    # --- sharding / storage ---
    shard_max_bytes: int = 1 * 1024 ** 3   # 1GB (chhoti machine ke liye 512MB bhi)
    rds_version: int = 1

    # --- filtering ---
    min_file_bytes: int = 8
    max_file_bytes: int = 1 * 1024 ** 2    # 1MB per file cap
    min_quality: int = 0                   # is quality-score se neeche wale drop
    dedup_files: bool = True
    dedup_chunks: bool = True

    # --- determinism ---
    seed: int = 1234

    # --- FIM (Fill-In-the-Middle) builder ---
    fim_enabled: bool = True
    fim_rate: float = 0.5           # kitne functions ko FIM banaya jaaye (0..1)
    fim_min_chars: int = 60         # itne se chhote functions skip

    # --- reproducibility / versioning ---
    dataset_version: str = "0.1.0"
    model_version: str = "ryth-30m"

    # --- cleaner knobs ---
    vendor_dirs: set = field(default_factory=lambda: set(DEFAULT_VENDOR_DIRS))
    generated_markers: tuple = DEFAULT_GENERATED_MARKERS
    allowed_ext: set = field(default_factory=lambda: set(DEFAULT_ALLOWED_EXT))

    # ---- derived: token storage dtype ----
    @property
    def token_typecode(self) -> str:
        # uint16 (H) 65536 vocab tak; usse zyada -> uint32 (I)
        return "H" if self.vocab_size <= 65536 else "I"

    @property
    def token_itemsize(self) -> int:
        return 2 if self.token_typecode == "H" else 4

    @property
    def dtype_flag(self) -> int:
        return 0 if self.token_typecode == "H" else 1
