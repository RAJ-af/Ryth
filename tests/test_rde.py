# ---------------- input layouts + empty-dataset guard ----------------
def _write(path, text):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as f:
        f.write(text)


def _build(root, out):
    tok = ByteTokenizer()
    cfg = RDEConfig(seq_len=64, vocab_size=tok.vocab_size, tokenizer_version=0)
    return RDEPipeline(tok, cfg).run(root, out, verbose=False, now_iso=NOW)


def test_flat_layout_single_file():
    """root/<file>.py (no repo subdir) must be discovered — the reported bug."""
    tmp = tempfile.mkdtemp()
    try:
        _write(os.path.join(tmp, "in", "sample.py"),
               "def greet(name):\n    return 'hi ' + name\n")
        m = _build(os.path.join(tmp, "in"), os.path.join(tmp, "out"))
        assert m["stats"]["files_kept"] == 1
        assert m["stats"]["total_files_seen"] == 1
        assert m["n_shards"] == 1
    finally:
        shutil.rmtree(tmp, ignore_errors=True)


def test_multi_repo_layout():
    """root/<repo>/<files> still works (no regression)."""
    tmp = tempfile.mkdtemp()
    try:
        _write(os.path.join(tmp, "in", "repoA", "a.py"), "def a():\n    return 1\n")
        _write(os.path.join(tmp, "in", "repoB", "b.py"), "def b():\n    return 2\n")
        m = _build(os.path.join(tmp, "in"), os.path.join(tmp, "out"))
        assert m["stats"]["files_kept"] == 2
    finally:
        shutil.rmtree(tmp, ignore_errors=True)


def test_mixed_layout_no_double_processing():
    """Loose file + subdir: each file discovered exactly once."""
    tmp = tempfile.mkdtemp()
    try:
        _write(os.path.join(tmp, "in", "top.py"), "def top():\n    return 1\n")
        _write(os.path.join(tmp, "in", "sub", "deep.py"), "def deep():\n    return 2\n")
        m = _build(os.path.join(tmp, "in"), os.path.join(tmp, "out"))
        assert m["stats"]["total_files_seen"] == 2      # deep.py NOT seen twice
        assert m["stats"]["files_kept"] == 2
    finally:
        shutil.rmtree(tmp, ignore_errors=True)


def test_empty_dir_raises():
    """Zero files discovered -> descriptive error, no empty dataset written."""
    tmp = tempfile.mkdtemp()
    try:
        os.makedirs(os.path.join(tmp, "in"))
        try:
            _build(os.path.join(tmp, "in"), os.path.join(tmp, "out"))
            assert False, "expected EmptyDatasetError"
        except EmptyDatasetError as e:
            assert "empty dataset" in str(e).lower()
    finally:
        shutil.rmtree(tmp, ignore_errors=True)


def test_all_filtered_raises():
    """Files exist but all filtered -> descriptive error mentioning drops."""
    tmp = tempfile.mkdtemp()
    try:
        with open(os.path.join(tmp, "bin.bin"), "wb") as f:
            f.write(b"\x00\x01\x02BINARY")
        os.makedirs(os.path.join(tmp, "in"), exist_ok=True)
        with open(os.path.join(tmp, "in", "bin.bin"), "wb") as f:
            f.write(b"\x00\x01\x02BINARY")
        try:
            _build(os.path.join(tmp, "in"), os.path.join(tmp, "out"))
            assert False, "expected EmptyDatasetError"
        except EmptyDatasetError as e:
            assert "filtered out" in str(e).lower()
    finally:
        shutil.rmtree(tmp, ignore_errors=True)


def test_not_a_directory_raises():
    tmp = tempfile.mkdtemp()
